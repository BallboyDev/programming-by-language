using System;

class sum{
	public static int Main{
		
		
		int num;
	
		num=Convert.ToInt32(Console.ReadLine());
		
		if(90<=num&&num<=100){
			Console.WriteLine("A");
		}
		
		if else(80<=num&&num<=89){
			Console.WriteLine("B");
		}
	
		if else(70<=num&&num<=79){
			Console.WriteLine("C");
		}
	
		if else(60<=num&&num<=69){
			Console.WriteLine("D");
		}
	
		else
			Console.WriteLine("F");
	
		return 0;
	}
}
	
