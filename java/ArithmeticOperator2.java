public class ArithmeticOperator2{
    public static void main(String args[]){
        // [1] 괄호 없음 
        System.out.println("[1] 1 + 1 = " + 1 + 1);

        // [2] 괄호 있음
        System.out.println("[2] 1 + 1 = " + (1 + 1));

        // [3] 수치가 왼쪽에 있을 때
        System.out.println(1 + 2 + "3");
    }
}